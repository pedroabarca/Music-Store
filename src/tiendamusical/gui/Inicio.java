    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Usuario u = new Usuario();
        UsuarioBO ubo = new UsuarioBO();
        String correo = new String(txtCorreo.getText());
        String contrasenna = new String(txtContrasenna.getPassword());
        u.setCorreo(correo);
        u.setContrasenna(contrasenna);
        try {
            if (ubo.autentificarUsuario(u).getContrasenna().equals(u.getContrasenna())) {
                FrmTienda ft = new FrmTienda(ubo.autentificarUsuario(u));
                ft.setVisible(true);
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Correo o Contrase침a Invalida!");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

